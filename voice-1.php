                                    <h4 class="heading-title pull-left">まず田中さんが英語とイタリア語両方を勉強しようと思ったきっかけを教えてください。</h4>
                                </header> 

                                <p>元から海外に興味があり、音楽を始める前から海外に行って仕事をしたいと考えていました。音大に入った後は、イタリア語の歌に触れる中で、イタリアに留学したいと考えるようになり、イタリア語を勉強しようという思いを持ちました。ただ現地においていきなりイタリア語でコミュニケーションをとるのは難しいと考え、今まで勉強してきた英語も勉強しようと思いました。英語が使えるようになれば、イタリアでのレッスンで先生のイタリア語が理解できなかった時に英語で質問ができ、現地でも問題なく意思疎通ができるのではないかと思ったからです。</p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">イタリア語はどのように音楽で生きると思いますか。</h4>
                                </header> 
        
                                <p>イタリア語で歌っている自分たちは、イタリア語を話せることで歌詞の意味も理解できますし、イタリア人ならではの感性やニュアンスを表現しやすいと思います。また発音も現地の人と同じような発音をできれば、うまくフレーズがつながるので、イタリア語に定期的に触れることの大切さを実感しています。細かいニュアンスの違いを徹底しているか、していないかで表現者としての差がついてくるのではないでしょうか。元々の声や歌の技術は勿論重要ですが、競争に残る人と残れない人の差を分けるものはそうしたニュアンスの差を拾って、そこを最後まで表現できるかどうかで決まると思います。それには言語力が大きく影響してくるはずです。イタリア語が話せない人は、イタリア的なニュアンスを表現する時に、曖昧な部分が出てきてしまうことが多いようです。</p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">Skitで 言語を習ったことで生活にプラスになったことはありますか。</h4>
                                </header> 
        
                                <p>まず、習った言語を聞き取れるようになりました。テレビなどでイタリア語やフランス語や英語など、複数の言語が使われているヨーロッパの映画見た時に、skitで勉強している英語とイタリア語はしっかりと聞き取れます。意味が分からない部分があっても、どういう単語が使われているか、どこからどこまでがイタリア語なのかをしっかり聞き取れることができるようになりました。また外国人の講師の方が学校に来て、講義をしている時に、通訳を待つ前に、何を言っているかがなんとなく分かり、なるほど、と思う回数が増えました。</p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">Skitの具体的な利用方法について教えてください。</h4>
                                </header> 
        
                                <p>イタリア語に関しては、ヤコポ先生が楽譜を持っていて、歌詞の解釈をイタリア人である彼と話し合いができます。　イタリアに行く時、イタリア語か英語を使ってやり取りすることがほとんどなので、ホテルを予約する時やホームステイの手配を行う時にどのような文章を作ればいいのか分からなくても、その疑問をそのままskitのトピックとして持ち込み、skitで学ぶことができる、というような使い方をしています。旅行に行きたいけれど、英語でメールを送らなくてはならない、そのような時に自分がやりたいことや伝えたいことを全部メモして、skitに持って行って、先生に相談をして、実際に文章を作って、それを応用して、実際にメールを送るというような方法をよく利用しています。そのほかにも日常において日本語で会話している時に少し英語やイタリア語を意識してみると、どのように言えば良いのだろうかというような疑問がどんどん湧いてきます。このような疑問はskitでのテーマにぴったりだと思います。 </p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">沢山英会話教室がある中でskitを選んだ理由について教えてください。</h4>
                                </header> 
        
                                <p>英語に関しては、授業で扱った内容を音声で録音して後から復習できるというシステムや、何より他の英会話教室と違うのは自分でテーマを決めて自分の話したいことを話せるということです。普通の英会話教室だと万人が使うことばかり勉強してしまいがちですが、skitだとちょっとした疑問をそのまま持ち込むことができ、細かなニュアンスについても教えてもらえるので、英語を自分のものとして身に付けられるということが一番大きな特徴だと思います。それにskitの講師陣は、授業中は勿論終わった後も日本語を使わないですね。以前通っていた英会話教室では、授業が終わってしまったら日本語で会話してしまうことがありました。日本語を使わないことで英語やイタリア語で上手くコミュニケーションを取れなかった時に、不安というか不完全燃焼のようなものが残るのですが、これは言語習得をするにあたって大切なことだと思います。もっと頑張らなければいけないという気持ちにさせてくれます。</p>

