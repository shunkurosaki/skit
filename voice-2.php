                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">何故数あるイタリア語教室の中で、SKITを選んでくださったのでしょうか。</h4>
                                </header> 
        
                                <p>まず値段の安さに惹かれました。他の教室は１時間当たり８０００円以上かかるところが多いのに対して、SKITは１時間３８００円で1:2の授業を受けれることができると知り、その安さに驚き体験授業を受けてみようと思いました。 授業内容については、入会前はよく分からなかったのですが、実際に授業を受けてみて、余計な自己紹介などの時間が少なく、自分に必要な場面でのイタリア語を使うことができ、少人数でフランクな環境で先生に質問ができるので、他の教室にはない魅力を感じ、入会することにしました。</p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">授業内容では実際にどのように学んでいるのですか。</h4>
                                </header> 
        
                                <p>私は最初から話したいことを決めて行くのではなく、最初の数分間に先生と会話を行い、その後その中から面白そうな内容を見つけて、それをトピックにして会話を行うようにしています。会話の中で、間違った発音をした場合は先生が訂正してくれますし、分からない文法や単語があれば気軽に質問することができます。そしてskitを作成する際は、自分で文章を書き込むため、どこがわかっていてどの部分を理解していないのかがよくわかり、書き込む作業を通じて楽しくイタリア語を学ぶことができています。</p><br>

                                <header class="page-heading clearfix">
                                    <h4 class="heading-title pull-left">SKITで学んだイタリア語は実際の生活でどう活きていますか。</h4>
                                </header> 
        
                                <p>声楽を学ぶ上で非常に役に立っています。SKITの授業で、定期的にイタリア語を使い、ネイティブの発音を聞いているので、よりネイティブに近い発音で歌うことができるようになったと思います。またネイティブの先生と接する中で、日本人とは異なる、イタリア人独特の感情表現の仕方を知ることができ、歌への感情の込め方が変わった実感があります。</p><br>

                                <header class="page-heading clearfix">
